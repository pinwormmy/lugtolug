import seed from "../data/watches.seed.json" with { type: "json" };
import brandSearchAliases from "../data/brand-search-aliases.json" with { type: "json" };
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { resolve } from "node:path";
// Shared with the app so runtime search text and the D1 search_text column
// cannot drift. Requires Node with TypeScript type stripping (>= 23.6).
import { buildWatchSearchText, getWatchModelSlug, slugify } from "../src/lib/watchText.ts";

const RETIRED_WATCH_IDS = [7, 14, 17, 18, 1462, 1463, 1464, 1468, 1469, 1933, 1972, 2001, 2002, 2761, 3100, 3105, 4463, 4477, 4486];
const brandArg = process.argv.find((argument) => argument.startsWith("--brand="));
const outputArg = process.argv.find((argument) => argument.startsWith("--output="));
const selectedBrand = brandArg?.slice("--brand=".length).trim() || null;
const scopedSeed = selectedBrand ? seed.filter((watch) => watch.brand === selectedBrand) : seed;

if (!scopedSeed.length) throw new Error(`No seed rows found${selectedBrand ? ` for brand ${selectedBrand}` : ""}.`);

const WATCH_COLUMNS = [
  "id",
  "brand",
  "model",
  "canonical_model",
  "model_group",
  "variant",
  "reference",
  "brand_slug",
  "model_slug",
  "reference_slug",
  "search_text",
  "lug_to_lug_mm",
  "case_mm",
  "thickness_mm",
  "lug_width_mm",
  "status"
];

function optionalString(value) {
  return typeof value === "string" && value.trim() ? value.trim() : null;
}

function sqlValue(value) {
  if (value == null) return "NULL";
  if (typeof value === "number") return Number.isFinite(value) ? String(value) : "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function watchRow(watch) {
  return [
    watch.id,
    watch.brand,
    watch.model,
    optionalString(watch.canonicalModel),
    optionalString(watch.modelGroup),
    optionalString(watch.variant),
    watch.reference,
    slugify(watch.brand),
    getWatchModelSlug(watch),
    slugify(watch.reference),
    buildWatchSearchText(watch, brandSearchAliases),
    watch.lugToLugMm,
    watch.caseMm,
    watch.thicknessMm,
    watch.lugWidthMm,
    "approved"
  ].map(sqlValue);
}

function sourceStatements() {
  const statements = [];

  for (const watch of scopedSeed) {
    for (const source of watch.sources) {
      const watchId = sqlValue(watch.id);
      const sourceUrl = sqlValue(source.sourceUrl);
      const note = sqlValue(source.note ?? null);

      statements.push(
        `UPDATE watch_sources SET note = ${note} WHERE watch_id = ${watchId} AND source_url = ${sourceUrl};`,
        [
          "INSERT INTO watch_sources (watch_id, source_url, note)",
          `SELECT ${watchId}, ${sourceUrl}, ${note}`,
          `WHERE NOT EXISTS (SELECT 1 FROM watch_sources WHERE watch_id = ${watchId} AND source_url = ${sourceUrl});`
        ].join("\n")
      );
    }
  }

  return statements;
}

const updateColumns = WATCH_COLUMNS.filter((column) => !["id", "status"].includes(column));

// D1 rejects statements above SQLite's maximum SQL length, so upsert in batches.
const WATCH_INSERT_BATCH_SIZE = 100;
const watchInsertStatements = [];
for (let index = 0; index < scopedSeed.length; index += WATCH_INSERT_BATCH_SIZE) {
  const batch = scopedSeed.slice(index, index + WATCH_INSERT_BATCH_SIZE);
  watchInsertStatements.push(
    [
      "INSERT INTO watches",
      `(${WATCH_COLUMNS.join(", ")})`,
      "VALUES",
      batch.map((watch) => `(${watchRow(watch).join(", ")})`).join(",\n"),
      "ON CONFLICT(id) DO UPDATE SET",
      [
        ...updateColumns.map((column) => `  ${column} = excluded.${column}`),
        "  status = CASE WHEN watches.status = 'approved' THEN excluded.status ELSE watches.status END",
        "  updated_at = CURRENT_TIMESTAMP"
      ].join(",\n") + ";"
    ].join("\n")
  );
}

const sql = [
  selectedBrand
    ? `-- Generated by scripts/generate-seed-sql.mjs for brand: ${selectedBrand}. Do not edit by hand.`
    : "-- Generated by npm run data:seed-sql. Do not edit by hand.",
  "",
  ...(selectedBrand
    ? []
    : [
        "-- Superseded seed rows merged into canonical records.",
        `UPDATE watches SET status = 'archived', updated_at = CURRENT_TIMESTAMP WHERE id IN (${RETIRED_WATCH_IDS.join(", ")}) AND status = 'approved';`,
        ""
      ]),
  "-- Deployed rows can still hold an older id layout; park their slugs on unique",
  "-- placeholders so the id-keyed upserts below cannot trip slug uniqueness mid-run.",
  `UPDATE watches SET reference_slug = 'seed-tmp-' || id WHERE id IN (${scopedSeed.map((watch) => watch.id).join(", ")});`,
  "",
  ...watchInsertStatements,
  "",
  "-- Idempotent source upserts keyed by watch_id and source_url.",
  ...sourceStatements(),
  ""
].join("\n");

const outputPath = outputArg ? resolve(process.cwd(), outputArg.slice("--output=".length)) : fileURLToPath(new URL("../data/seed.sql", import.meta.url));
writeFileSync(outputPath, sql);
