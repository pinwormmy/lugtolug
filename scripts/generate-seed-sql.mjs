import seed from "../data/watches.seed.json" with { type: "json" };
import { writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

const RETIRED_WATCH_IDS = [1933, 1972, 2001, 2002];

const WATCH_COLUMNS = [
  "id",
  "brand",
  "model",
  "canonical_model",
  "model_group",
  "variant",
  "reference",
  "brand_slug",
  "model_slug",
  "reference_slug",
  "search_text",
  "lug_to_lug_mm",
  "case_mm",
  "thickness_mm",
  "lug_width_mm",
  "status"
];

const METRICS = [
  ["lugToLugMm", "Lug-to-lug"],
  ["caseMm", "Case"],
  ["thicknessMm", "Thickness"],
  ["lugWidthMm", "Lug width"]
];

function slugify(value) {
  return String(value)
    .trim()
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function normalizeSearch(value) {
  return String(value)
    .trim()
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z0-9]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function transliterateGermanSearch(value) {
  return String(value).toLowerCase().replace(/ä/g, "ae").replace(/ö/g, "oe").replace(/ü/g, "ue").replace(/ß/g, "ss");
}

function normalizeSearchWithAliases(value) {
  const normalized = normalizeSearch(value);
  const germanTransliterated = normalizeSearch(transliterateGermanSearch(value));

  return [normalized, germanTransliterated]
    .filter((part, index, parts) => part.length > 0 && parts.indexOf(part) === index)
    .join(" ");
}

function optionalString(value) {
  return typeof value === "string" && value.trim() ? value.trim() : null;
}

function getWatchSearchText(watch) {
  const metricText = METRICS.flatMap(([key, label]) => {
    const value = watch[key];
    return value == null ? [] : [label, String(value), `${value}mm`];
  }).join(" ");

  return normalizeSearchWithAliases(
    `${watch.brand} ${watch.model} ${watch.canonicalModel ?? ""} ${watch.modelGroup ?? ""} ${watch.variant ?? ""} ${watch.reference} ${metricText}`
  );
}

function sqlValue(value) {
  if (value == null) return "NULL";
  if (typeof value === "number") return Number.isFinite(value) ? String(value) : "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function watchRow(watch) {
  return [
    watch.id,
    watch.brand,
    watch.model,
    optionalString(watch.canonicalModel),
    optionalString(watch.modelGroup),
    optionalString(watch.variant),
    watch.reference,
    slugify(watch.brand),
    slugify(watch.model),
    slugify(watch.reference),
    getWatchSearchText(watch),
    watch.lugToLugMm,
    watch.caseMm,
    watch.thicknessMm,
    watch.lugWidthMm,
    "approved"
  ].map(sqlValue);
}

function sourceStatements() {
  const statements = [];

  for (const watch of seed) {
    for (const source of watch.sources) {
      const watchId = sqlValue(watch.id);
      const sourceUrl = sqlValue(source.sourceUrl);
      const note = sqlValue(source.note ?? null);

      statements.push(
        `UPDATE watch_sources SET note = ${note} WHERE watch_id = ${watchId} AND source_url = ${sourceUrl};`,
        [
          "INSERT INTO watch_sources (watch_id, source_url, note)",
          `SELECT ${watchId}, ${sourceUrl}, ${note}`,
          `WHERE NOT EXISTS (SELECT 1 FROM watch_sources WHERE watch_id = ${watchId} AND source_url = ${sourceUrl});`
        ].join("\n")
      );
    }
  }

  return statements;
}

const updateColumns = WATCH_COLUMNS.filter((column) => !["id", "status"].includes(column));
const watchValues = seed.map((watch) => `(${watchRow(watch).join(", ")})`).join(",\n");

const sql = [
  "-- Generated by npm run data:seed-sql. Do not edit by hand.",
  "",
  "-- Superseded seed rows merged into canonical records.",
  `UPDATE watches SET status = 'archived', updated_at = CURRENT_TIMESTAMP WHERE id IN (${RETIRED_WATCH_IDS.join(", ")}) AND status = 'approved';`,
  "",
  "INSERT INTO watches",
  `(${WATCH_COLUMNS.join(", ")})`,
  "VALUES",
  watchValues,
  "ON CONFLICT(id) DO UPDATE SET",
  [
    ...updateColumns.map((column) => `  ${column} = excluded.${column}`),
    "  status = CASE WHEN watches.status = 'approved' THEN excluded.status ELSE watches.status END",
    "  updated_at = CURRENT_TIMESTAMP"
  ].join(",\n") + ";",
  "",
  "-- Idempotent source upserts keyed by watch_id and source_url.",
  ...sourceStatements(),
  ""
].join("\n");

const outputPath = fileURLToPath(new URL("../data/seed.sql", import.meta.url));
writeFileSync(outputPath, sql);
